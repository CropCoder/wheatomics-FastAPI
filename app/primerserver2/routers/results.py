import re
from pathlib import Path

from fastapi import APIRouter, Depends, HTTPException
from fastapi.responses import FileResponse, HTMLResponse

from ..services.storage import JobManager
from .jobs import get_job_manager

router = APIRouter(prefix="", tags=["PrimerServer2"])

# Safe filename pattern: disallow path separators, control chars, and leading dots.
_SAFE_FILENAME_RE = re.compile(r"^[A-Za-z0-9_.\-]+$")


def _secure_filename(filename: str) -> str:
    """Normalize and validate a filename to prevent path traversal."""
    if not filename:
        raise HTTPException(status_code=400, detail="Filename is required")
    # Reject any path components or suspicious characters.
    if "/" in filename or "\\" in filename or filename.startswith("..") or filename.startswith("."):
        raise HTTPException(status_code=400, detail="Invalid filename")
    if not _SAFE_FILENAME_RE.match(filename):
        raise HTTPException(status_code=400, detail="Invalid filename characters")
    return filename


@router.get(
    "/jobs/{job_id}/result-html",
    summary="Get legacy HTML result",
    description="Returns the raw legacy HTML result page if it was generated by the pipeline.",
)
def get_result_html(
    job_id: str,
    job_manager: JobManager = Depends(get_job_manager),
):
    """Legacy endpoint that returns the raw HTML result if available."""
    status = job_manager.get_status(job_id)
    if status is None:
        raise HTTPException(status_code=404, detail="Job not found")
    if status["status"] != "done":
        raise HTTPException(status_code=400, detail=f"Job status is {status['status']}")

    job_dir = job_manager.get_job_dir(job_id)
    result_html = job_dir / "primer.final.result.html"
    check_html = job_dir / "specificity.check.result.html"

    if result_html.exists():
        return HTMLResponse(content=result_html.read_text(encoding="utf-8"))
    if check_html.exists():
        return HTMLResponse(content=check_html.read_text(encoding="utf-8"))

    raise HTTPException(status_code=404, detail="Result HTML not found")


@router.get(
    "/jobs/{job_id}/specificity/{filename}",
    summary="Download a specificity result file",
    description="Downloads a file from the job's specificity-check result directory (e.g. alignment images).",
)
def get_specificity_result(
    job_id: str,
    filename: str,
    job_manager: JobManager = Depends(get_job_manager),
):
    status = job_manager.get_status(job_id)
    if status is None:
        raise HTTPException(status_code=404, detail="Job not found")

    filename = _secure_filename(filename)

    job_dir = job_manager.get_job_dir(job_id)
    result_dir = job_dir / "result.specificity.check"
    file_path = result_dir / filename

    # Defensive path traversal check.
    try:
        file_path.resolve().relative_to(result_dir.resolve())
    except ValueError:
        raise HTTPException(status_code=400, detail="Invalid filename")

    if not file_path.exists() or not file_path.is_file():
        raise HTTPException(status_code=404, detail="Specificity result not found")

    return FileResponse(file_path)
